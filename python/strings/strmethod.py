#string methods
a = "hello";
print(a.upper());
print(a.lower());
print(a.strip());
print(a.replace("h", "j"));
print(a.split(","));

#casefold()
a = "Hello, World!";
print(a.casefold());

#center()
a = "Hello, World!";
print(a.center(20));

#count()
a = "Hello, World!";
print(a.count("o"));

#endswith()
a = "Hello, World!";
print(a.endswith("!"));

