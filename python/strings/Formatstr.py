#format string
age = 20;
txt = f"my age is {age}";
print(txt);

#modifier 
price = 49.95;
txt = f"the price is {price:.2f}";
print(txt);


#Escape Character
txt = "we are the so-called \"vikings\" from the north.";
print(txt);

#Backslash
txt = "this is a backslash: \\";
print(txt);

#Newline
txt = "hello \n world";
print(txt);

#Carriage Return
txt = "hello \r world";
print(txt);

#Tab
txt = "hello \t world";
print(txt);

#Backspace
txt = "hello \b world";
print(txt);

#Form Feed
txt = "hello \f world";
print(txt);

#Octal Value
txt = "\110\145\154\154\157";
print(txt);

#Hex Value
txt = "\x48\x65\x6c\x6c\x6f";
print(txt);
