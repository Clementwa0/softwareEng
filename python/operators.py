#arithmetic operators
a = 10;
b = 20;
print(a + b); #addition 
print(a - b); #subtraction
print(a * b); #multiplication
print(a / b); #division

#floor division
print(a // b);

#modulus
print(a % b);

#assignment operators
a = 10;
b = 20;
a += b; #a = a + b
print(a);

#comparison operators
a = 10;
b = 20;
print(a == b); #equal   
print(a != b); #not equal
print(a > b); #greater than
print(a < b); #less than
print(a >= b); #greater than or equal to
print(a <= b); #less than or equal to

#logical operators
a = True;
b = False;
print(a and b); #and true if both are true
print(a or b); #or true if one of the two is true
print(not a); #not true if a is false

#identity operators
a = 10;
b = 20;
print(a is b); #is true if a and b are the same object
print(a is not b); #is not true if a and b are not the same object

#membership operators
a = "hello";
b = "hello";
print(a in b); #in true if a is found in b
print(a not in b); #not in true if a is not found in b

#bitwise operators
a = 10;
b = 20;
print(a & b); #and
print(a | b); #or
print(a ^ b); #xor
print(~a); #not
print(a << b); #left shift
print(a >> b); #right shift

